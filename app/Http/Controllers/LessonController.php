     */
    public function store(LessonRequest $request)
    {
        $lesson= new Lesson();
        $lesson->name = $request->name;
        $lesson->content = $request->content;
        $lesson->group_id =$request->group_id;
        $lesson ->save();

        return redirect()->back();
    }

